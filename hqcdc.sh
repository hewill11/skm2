#!/bin/bash
set -euo pipefail

# Назначение: Указать DNS на HQ-SRV и проверить доступность сайта с HQ-CLI
# Билет / задание: Задание 7 (часть HQ-CLI)
# ОС: Debian 13
# Основано на: методичка (resolv.conf nameserver 192.168.100.2, http://192.168.100.2/)
# ВНИМАНИЕ: правка resolv.conf временная — обычно перезаписывается DHCP/NetworkManager.
#           Надёжнее обновить аренду DHCP (dhclient) после настройки HQ-RTR.

if [ "$EUID" -ne 0 ]; then
  echo "Запусти от root"
  exit 1
fi

DNS_IP="192.168.100.2"

# Временно прописываем DNS (как на скрине)
echo "nameserver ${DNS_IP}" > /etc/resolv.conf

echo "OK"

# ==============================
# ПРОВЕРКА РЕЗУЛЬТАТА
# ==============================
# cat /etc/resolv.conf
# curl -I http://192.168.100.2/
# в браузере: http://192.168.100.2/

# ==============================
# РУЧНОЕ ВЫПОЛНЕНИЕ ЗАДАНИЯ
# ==============================
# 1. nano /etc/resolv.conf
#    nameserver 192.168.100.2
# 2. Проверить сайт: http://192.168.100.2/
#    (или обновить аренду DHCP: dhclient -r && dhclient)

# ==============================
# ИЗМЕНЁННЫЕ ФАЙЛЫ
# ==============================
